    // Load lessons of the selected course
   private void loadLessons() {
        model.setRowCount(0);

        Course course = accountManager.getCourseById(courseId);
        if (course == null) return;

        List<Lesson> lessons = course.getLessons();
        for (Lesson lesson : lessons) {
            boolean completed = false;
            if (currentUser instanceof Student) {
                Student student = (Student) currentUser;
                completed = student.isLessonCompleted(courseId, lesson.getLessonId());
            }
            
            // Convert the list of resources into a readable string
            // Example: "video.mp4, slides.pdf"
            String resourcesString = "";
            if (lesson.getResources() != null && !lesson.getResources().isEmpty()) {
                resourcesString = String.join(", ", lesson.getResources());
            } else {
                resourcesString = "None";
            }
            
            // Add the row with the new "resourcesString"
            model.addRow(new Object[]{
                lesson.getLessonId(), 
                lesson.getTitle(), 
                lesson.getContent(), 
                resourcesString, 
                completed
            });
        }
    }

