    private void approveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Please select a course to approve.", 
                "Selection Error", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Get the corresponding Course object from the list
        Course selectedCourse = pendingCourses.get(selectedRow);

        // Set the status to APPROVED and update the JSON file
        boolean success = adminManager.setCourseStatus(selectedCourse.getCourseId(), ApprovalStatus.APPROVED);

        if (success) {
            JOptionPane.showMessageDialog(this, 
                "Course \"" + selectedCourse.getTitle() + "\" APPROVED.", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            // Refresh the table to show remaining pending courses
            loadPendingCoursesTable();
        } else {
            JOptionPane.showMessageDialog(this, 
                "Failed to approve course. Course ID not found or error occurred.", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_approveActionPerformed

