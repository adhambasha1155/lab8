    private void loadPendingCoursesTable() {
        // 1. Get pending courses
        pendingCourses = adminManager.getPendingCourses();

        // 2. Define table columns
        String[] columnNames = {"Course ID", "Title", "Description", "Instructor Username", "Lessons"};

        // 3. Prepare data for the table model
        DefaultTableModel model = new DefaultTableModel(columnNames, 0) {
            // Make cells non-editable
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        for (Course course : pendingCourses) {
            String instructorUsername = "N/A";
            
            User instructor = accountManager.getUserById(course.getInstructorId());
            if (instructor != null) {
                instructorUsername = instructor.getUsername();
            } else {
                // Fallback: show the ID if username can't be fetched
                instructorUsername = course.getInstructorId() + " (User Not Found)";
            }
            
            Object[] row = new Object[]{
                course.getCourseId(),
                course.getTitle(),
                course.getDescription(),
                instructorUsername,
                course.getLessons().size()
            };
            model.addRow(row);
        }
        
        // 4. Set the model to the table
        jTable1.setModel(model);
    }

